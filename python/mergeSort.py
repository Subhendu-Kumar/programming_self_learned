def merge_sort(arr):
    if len(arr) > 1:
        mid = len(arr) // 2
        L = arr[:mid]
        R = arr[mid:]

        merge_sort(L)
        merge_sort(R)

        i = j = k = 0

        while i < len(L) and j < len(R):
            if L[i] < R[j]:
                arr[k] = L[i]
                i += 1
            else:
                arr[k] = R[j]
                j += 1
            k += 1

        while i < len(L):
            arr[k] = L[i]
            i += 1
            k += 1

        while j < len(R):
            arr[k] = R[j]
            j += 1
            k += 1

    return arr


arr = []
x = int(input("Enter the no of elements of the array : "))
print("Enter the elements of array one by one : ")

for i in range(x):
    n = int(input())
    arr.append(n)
sorted_arr = merge_sort(arr)
print("Sorted array is:", sorted_arr)
